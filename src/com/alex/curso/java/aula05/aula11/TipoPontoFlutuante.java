package com.alex.curso.java.aula05.aula11;

public class TipoPontoFlutuante {
    public static void main(String[] args) {
        /*
         * Tipos ponto flutuante
         * float = 32 bits
         * double = 64 bits
         * O tipo mais usado em projetos é tipo double
         */

        // exemplo de declaração
        float saldo1 = 100.30f;
        System.out.println(saldo1);

        double saldo2 = 100.30;
        System.out.println(saldo2);
    }
}
