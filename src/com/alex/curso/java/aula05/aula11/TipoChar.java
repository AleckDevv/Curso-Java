package com.alex.curso.java.aula05.aula11;

public class TipoChar {
    public static void main(String[] args) {
        /*
         * O tipo char serve para armazenar um único caractere do alfabeto
         */

        char sexo1 = 'm';
        System.out.println(sexo1);

        char sexo2 = 'f';
        System.out.println(sexo2);

        /*
         * O char pode armazenar também valores inteiros
         * se fizer a seguite declaração, ela vai funcionar
         */

        char o = 111;
        System.out.println(o);

        char i = 105;
        System.out.println(i);
        System.out.println("" + 0 + i);
    }
}
