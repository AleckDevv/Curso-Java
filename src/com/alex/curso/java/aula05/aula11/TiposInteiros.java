package com.alex.curso.java.aula05.aula11;

public class TiposInteiros {
    public static void main(String[] args) {
        /*
         * Variáveis e Tipos Primitivos
         * Tipos inteiros
         * Tipos Ponto Flutuante
         * Tipo Char
         * Tipo Boolean
         * Literais
         * 
         * Tipos Inteiros:
         * byte = 8 bits -> -126 a 127
         * short = 16 bits -> -32.768 a 32.767
         * int = 32 bits -> -2.147.483.648 a 2.147.483.647
         * long = 64 bits -> -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807
         * Os Tipos mais usados em projetos reais no dia a dia dos grupos inteiros são:
         * int e long
         */

        // exemplo de declaração tipos inteiros
        byte idade1 = 20;
        System.out.println(idade1);

        short idade2 = 20;
        System.out.println(idade2);

        int idade3 = 20;
        System.out.println(idade3);

        long idade4 = 20;
        System.out.println(idade4);

    }
}
